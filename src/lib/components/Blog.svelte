				A Diagnosis of Stage 4 Breast Cancer and OI
			</div>
			<div class="pt-8 font-georgia text-left text-md text-black leading-10">
				A 46-year old female was diagnosed last September 2022 with stage 4 breast cancer and
				brittle bone disease or Osteogenesis imperfecta (OI), a disease in which bones fracture or
				break easily often with no injury or cause (National Institutes of Health). She and her
				sister went to the University of Cebu’s Medical Center (UCMed) for treatment but the doctors
				said that she was already in critical condition and would require immediate laser treatment
				for her bones and chemotherapy for her cancer. If not, the doctors said she can only live
				for at least 3-4 months after the diagnosis. According to her, the treatments cost 50,000
				pesos per session. Because they could not afford it, one of her elder sisters suggested the
				faith healer, ‘Day Fe from Jagobiao. Before she went to ‘Day Fe for healing, she could not
				stand up or walk on her own and she was bedridden most of the time. ‘Day Fe observed and
				healed her for 115 days. By April 2023, she could now stand up and walk on her own, the cyst
				on her breast had become smaller as months passed by, and she is now in lesser pain compared
				to her condition 7 months ago.
			</div>
