			I first encountered mystical women who practice alternative healing in 2005. I was six years
			old. As a child, I was prone to coughs that lasted for weeks. Store-bought medicines couldn’t
			help me, so my mother took me to a traditional healer, an old woman, who gave me a
			fuchsia-colored tonic to drink for a couple of days. It was made from a certain bark of a tree
			that she grew in her yard. Whatever her story was or whether she was a mystic healer or not,
			my coughs suddenly stopped because of that tonic she gave me and I was able to live a simple
			childhood. I never saw her again, but the curiosity I have for those types of people remained
			with me. It was not until my father started coughing blood in 2012 that my family decided to
			reach out to another healer in Lower Jagobiao, Mandaue City in Sitio Lourdes. The healer is
			recognized as Inday Fe or ‘Day Fe.
