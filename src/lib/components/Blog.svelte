				life that day. When ‘Day Fe told them this, the second young woman cried. How could ‘Day Fe
				knew about it when she never told anyone? In ‘Day Fe’s vision, she saw this young woman
				floating dead in the waters after jumping from the Marcelo Fernan bridge. She told the young
				woman to give up the thought of suicide and instead go to Ayala Center Cebu’s Bo’s Coffee
				with the first young woman. ‘Day Fe said that they will meet their future husbands there.
				The second young woman didn’t believe this at first but she eventually followed what ‘Day Fe
				told them. While sipping their coffees in the coffee shop the next day, two foreigners
				approached the young women. Today, they are both married. ‘Day Fe and her husband were
				invited to the wedding but unfortunately, they weren’t able to attend because of ‘Day Fe’s
				hectic schedule that day.
