				‘Day Fe’s method
			</div>
			<div class="pt-8 font-georgia text-left text-md text-black leading-10">
				Based on my observations back then, ‘Day Fe’s practices were different from the methods of
				other traditional healers. The local healers that I know of relied on their ancestors,
				elves, and “dili-ingon nato” (in Cebuano dialect) or supernatural nature beings for their
				ability to heal (as what they claim). But ‘Day Fe depends on the Virgin Mary and the Holy
				Trinity for what she does. Aside from healing, ‘Day Fe is also a clairvoyant (which means
				she can look into a person and instantly know who the person is, what they’re feeling, what
				they desire, and what their possible future looks like).

				<br />
				<br />

				This is how ‘Day Fe’s method of healing goes: First, she will ask the individual to confess
				their sins. Second, she will say a prayer and begin the procedure by putting her hand on the
				specific area of the body where the discomfort is felt or where the source of the illness is
				located. The amount of time of the healing would depend on how severe the pain is. When the
				session is over, ‘Day Fe recommends ways to help the person heal faster such as using her
				blessed water, avoiding certain types of food, avoiding unpleasant emotions (like anger and
				worry), wearing a scapular or other religious paraphernalia, regular prayers, and following
				her suggested number of days of healing.
